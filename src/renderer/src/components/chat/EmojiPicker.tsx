const ALL_EMOJIS = [...new Set(Object.values(EMOJI_CATEGORIES).flat())]

interface Props {
  onClose: () => void
  onInsert: (emoji: string) => void
}

export default function EmojiPicker({ onClose, onInsert }: Props) {
  const [q, setQ] = useState('')
  const inputRef = useRef<HTMLInputElement>(null)

  useEffect(() => { inputRef.current?.focus() }, [])

  const handleKeyDown = useCallback((e: React.KeyboardEvent) => {
    if (e.key === 'Escape') onClose()
  }, [onClose])

  const filtered = q.trim()
    ? ALL_EMOJIS.filter((e) => EMOJI_KEYWORDS[e]?.some(k => k.includes(q.toLowerCase())))
    : null

  return (
    <div
      className="bg-bg-panel border border-border-md rounded-card shadow-2xl w-80 overflow-hidden"
      style={{ background: 'rgba(15,18,28,0.97)', border: '1px solid rgba(59,67,96,0.4)' }}
    >
      <div className="p-2 border-b border-border-lo">
        <input
          ref={inputRef}
          value={q}
          onChange={(e) => setQ(e.target.value)}
          onKeyDown={handleKeyDown}
          placeholder="Search emojis..."
          className="input-field text-sm"
        />
      </div>
      <div className="max-h-64 overflow-y-auto p-2">
        {filtered ? (
          <div className="grid grid-cols-8 gap-0.5">
            {filtered.map((e) => (
              <button
                key={e}
                onClick={() => onInsert(e)}
                className="w-8 h-8 flex items-center justify-center text-lg rounded hover:bg-bg-hover transition-colors"
              >
                {e}
              </button>
            ))}
            {filtered.length === 0 && (
              <div className="col-span-8 text-center text-text-lo text-xs py-4">No emojis found</div>
            )}
          </div>
        ) : (
          Object.entries(EMOJI_CATEGORIES).map(([cat, emojis]) => (
            <div key={cat} className="mb-3">
              <p className="text-text-lo text-xs font-body uppercase tracking-wider mb-1 px-1">{cat}</p>
              <div className="grid grid-cols-8 gap-0.5">
                {[...new Set(emojis)].map((e) => (
                  <button
                    key={e}
                    onClick={() => onInsert(e)}
                    className="w-8 h-8 flex items-center justify-center text-lg rounded hover:bg-bg-hover transition-colors"
                    title={e}
                  >
                    {e}
                  </button>
                ))}
              </div>
            </div>
          ))
        )}
      </div>
    </div>
  )
}

export function insertEmoji(textarea: HTMLTextAreaElement | null, emoji: string) {
  if (!textarea) return
  const nativeInputValueSetter = Object.getOwnPropertyDescriptor(window.HTMLTextAreaElement.prototype, 'value')?.set
  const start = textarea.selectionStart
  const end = textarea.selectionEnd
  const val = textarea.value
  const next = val.slice(0, start) + emoji + val.slice(end)
  nativeInputValueSetter?.call(textarea, next)
  textarea.dispatchEvent(new Event('input', { bubbles: true }))
  textarea.selectionStart = textarea.selectionEnd = start + emoji.length
}